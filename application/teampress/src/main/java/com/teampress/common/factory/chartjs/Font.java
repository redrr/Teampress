package com.teampress.common.factory.chartjs;

public class Font {
    private Color color;
    private String family, style;
    private Integer size, weight, lineWidth;
    private Double lineHeight;

    Color getColor() {
        return color;
    }

    String getFamily() {
        return family;
    }

    String getStyle() {
        return style;
    }

    Integer getSize() {
        return size;
    }

    Integer getWeight() {
        return weight;
    }

    Integer getLineWidth() {
        return lineWidth;
    }

    Double getLineHeight() {
        return lineHeight;
    }
}
