package hu.playmaker.form;

public class LeagueForm extends BaseForm {

    public String evad;
    public String szervezo;
    public String liga;
    public String klubName;
    public String team;

    public String getEvad() {
        return evad;
    }

    public void setEvad(String evad) {
        this.evad = evad;
    }

    public String getSzervezo() {
        return szervezo;
    }

    public void setSzervezo(String szervezo) {
        this.szervezo = szervezo;
    }

    public String getLiga() {
        return liga;
    }

    public void setLiga(String liga) {
        this.liga = liga;
    }

    public String getKlubName() {
        return klubName;
    }

    public void setKlubName(String klubName) {
        this.klubName = klubName;
    }

    public String getTeam() {
        return team;
    }

    public void setTeam(String team) {
        this.team = team;
    }
}
